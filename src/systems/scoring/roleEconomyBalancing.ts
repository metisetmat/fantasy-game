export function roleUsageAuditRows(leagueTable: LeagueTableIntegrationSummary): readonly string[] {
  return TRUE_ROLE_DEFINITIONS.map((role) => {
    const roleToken = firstRoleToken(role.name).toLowerCase();
    const loadRows = leagueTable.playerMatchLoadSummaries.filter((row) => row.role.toLowerCase().includes(roleToken));
    const rosterPresenceRate = role.family === "goalkeeper" ? 100 : role.economyStatus === "STYLE_SPECIFIC_HEALTHY" ? 62 : 78;
    const actionInvolvement = loadRows.length > 0 ? average(loadRows.map((row) => numeric(row.lateMatchActionLoad) + numeric(row.repeatedEffortLoad))) : role.family === "goalkeeper" ? average(leagueTable.playerMatchLoadSummaries.map((row) => numeric(row.goalkeeperRecoveryLoad))) : 12;
    const routeInvolvement = role.family === "offensive" ? "HIGH" : role.family === "goalkeeper" ? "LOW" : "CONTEXTUAL";
    const defensiveInvolvement = role.family === "defensive" || role.family === "goalkeeper" ? "HIGH" : role.family === "endurance" ? "MEDIUM" : "LOW";
    const fatigueCost = role.fatigueLoadProfile.includes("very high") || role.fatigueLoadProfile.includes("high") ? "HIGH" : role.fatigueLoadProfile.includes("medium") ? "MEDIUM" : "LOW";
    const bonusContribution = role.primaryRouteContribution.includes("TRY") || role.primaryRouteContribution.includes("DROP") || role.primaryRouteContribution.includes("shot") || role.defensiveContribution.includes("major-threat") ? "VISIBLE" : "INDIRECT";
    return `| ${role.name} | ${rosterPresenceRate}% | ${Math.max(0, rosterPresenceRate - 8)}% | ${role.family === "endurance" ? 54 : 36}% | ${actionInvolvement} | ${routeInvolvement} | ${defensiveInvolvement} | ${fatigueCost} | ${bonusContribution} | ${role.economyStatus} |`;
  });
}

